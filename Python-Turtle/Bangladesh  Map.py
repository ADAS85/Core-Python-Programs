import turtle
turtle.colormode(255)
turtle.Screen().bgcolor("sky blue")
t = turtle.Turtle()
t.penup()
t.speed(2)
t.pencolor("gray")
t.setx(-150)
t.sety(100)
t.fillcolor(0,106,78)
t.begin_fill()
t.pendown()
t.forward(300)
t.right(90)
t.forward(150)
t.right(90)
t.forward(300)
t.right(90)
t.forward(150)
t.right(90)
t.end_fill()

t.penup()
t.setx(0)
t.sety(-25)

t.fillcolor(244,42,65)
t.begin_fill()
t.pendown()
t.circle(50)
t.end_fill()

